package e.himanshu.loginmodule;

public class HELP {
    private String Name;
    private String PHN;
    private String LOC;
    private String PSTMSSG;
    private String LOCLINK;



    public HELP() {
    }

    public String getLOCLINK() {
        return LOCLINK;
    }

    public void setLOCLINK(String LOCLINK) {
        this.LOCLINK = LOCLINK;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getPHN() {
        return PHN;
    }

    public void setPHN(String PHN) {
        this.PHN = PHN;
    }

    public String getLOC() {
        return LOC;
    }

    public void setLOC(String LOC) {
        this.LOC = LOC;
    }

    public String getPSTMSSG() {
        return PSTMSSG;
    }

    public void setPSTMSSG(String PSTMSSG) {
        this.PSTMSSG = PSTMSSG;
    }
}
